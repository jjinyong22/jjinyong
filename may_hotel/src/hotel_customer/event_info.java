package hotel_customer;

public class event_info {
	String event_id;
	String event_name;
	String event_info;
	String event_begin;
	String event_close;
	String event_image;
	public String getEvent_id() {
		return event_id;
	}
	public void setEvent_id(String event_id) {
		this.event_id = event_id;
	}
	public String getEvent_name() {
		return event_name;
	}
	public void setEvent_name(String event_name) {
		this.event_name = event_name;
	}
	public String getEvent_info() {
		return event_info;
	}
	public void setEvent_info(String event_info) {
		this.event_info = event_info;
	}
	public String getEvent_begin() {
		return event_begin;
	}
	public void setEvent_begin(String event_begin) {
		this.event_begin = event_begin;
	}
	public String getEvent_close() {
		return event_close;
	}
	public void setEvent_close(String event_close) {
		this.event_close = event_close;
	}
	public String getEvent_image() {
		return event_image;
	}
	public void setEvent_image(String event_image) {
		this.event_image = event_image;
	}
	
	
	
}
