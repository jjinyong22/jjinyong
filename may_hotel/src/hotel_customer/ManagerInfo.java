package hotel_customer;

public class ManagerInfo {
	private String man_id;
	private String man_pw;
	private String man_name;
	private String man_hiredate;
	private String man_phone;
	private String man_dept;
	public String getMan_id() {
		return man_id;
	}
	public void setMan_id(String man_id) {
		this.man_id = man_id;
	}
	public String getMan_pw() {
		return man_pw;
	}
	public void setMan_pw(String man_pw) {
		this.man_pw = man_pw;
	}
	public String getMan_name() {
		return man_name;
	}
	public void setMan_name(String man_name) {
		this.man_name = man_name;
	}
	public String getMan_hiredate() {
		return man_hiredate;
	}
	public void setMan_hiredate(String man_hiredate) {
		this.man_hiredate = man_hiredate;
	}
	public String getMan_phone() {
		return man_phone;
	}
	public void setMan_phone(String man_phone) {
		this.man_phone = man_phone;
	}
	public String getMan_dept() {
		return man_dept;
	}
	public void setMan_dept(String man_dept) {
		this.man_dept = man_dept;
	}
}
