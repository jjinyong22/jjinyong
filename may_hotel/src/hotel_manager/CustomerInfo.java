package hotel_manager;

public class CustomerInfo {
	private String cus_id;
	private String cus_pw;
	private String cus_name;
	private String cus_gen;
	private String cus_joindate;
	private String cus_phone;
	private String cus_email;
	public String getCus_id() {
		return cus_id;
	}
	public void setCus_id(String cus_id) {
		this.cus_id = cus_id;
	}
	public String getCus_pw() {
		return cus_pw;
	}
	public void setCus_pw(String cus_pw) {
		this.cus_pw = cus_pw;
	}
	public String getCus_name() {
		return cus_name;
	}
	public void setCus_name(String cus_name) {
		this.cus_name = cus_name;
	}
	public String getCus_gen() {
		return cus_gen;
	}
	public void setCus_gen(String cus_gen) {
		this.cus_gen = cus_gen;
	}
	public String getCus_joindate() {
		return cus_joindate;
	}
	public void setCus_joindate(String cus_joindate) {
		this.cus_joindate = cus_joindate;
	}
	public String getCus_phone() {
		return cus_phone;
	}
	public void setCus_phone(String cus_phone) {
		this.cus_phone = cus_phone;
	}
	public String getCus_email() {
		return cus_email;
	}
	public void setCus_email(String cus_email) {
		this.cus_email = cus_email;
	}
}
