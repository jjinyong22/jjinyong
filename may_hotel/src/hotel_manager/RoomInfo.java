package hotel_manager;

public class RoomInfo {
	private Integer room_id;
	private String room_type;
	private Integer room_price;
	private String room_check;
	private Integer room_head;
	private String room_image;
	public Integer getRoom_id() {
		return room_id;
	}
	public void setRoom_id(Integer room_id) {
		this.room_id = room_id;
	}
	public String getRoom_type() {
		return room_type;
	}
	public void setRoom_type(String room_type) {
		this.room_type = room_type;
	}
	public Integer getRoom_price() {
		return room_price;
	}
	public void setRoom_price(Integer room_price) {
		this.room_price = room_price;
	}
	public String getRoom_check() {
		return room_check;
	}
	public void setRoom_check(String room_check) {
		this.room_check = room_check;
	}
	public Integer getRoom_head() {
		return room_head;
	}
	public void setRoom_head(Integer room_head) {
		this.room_head = room_head;
	}
	public String getRoom_image() {
		return room_image;
	}
	public void setRoom_image(String room_image) {
		this.room_image = room_image;
	}
	
}
